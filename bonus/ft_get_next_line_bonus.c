char	*ft_strjoin(char const *s1, char const *s2)
{
	int		i;
	int		j;
	char	*str_malloc;

	str_malloc = NULL;
	i = 0;
	j = 0;
	if (s1 == NULL || s2 == NULL)
		return (NULL);
	i = ft_strlen(s1);
	j = ft_strlen(s2);
	str_malloc = (char *)malloc((i + j + 1) * sizeof(char));
	if (str_malloc == NULL)
		return (NULL);
	i = 0;
	j = 0;
	while (s1[i])
	{
		str_malloc[i] = s1[i];
		i++;
	}
	while (s2[j])
		str_malloc[i++] = s2[j++];
	str_malloc[i] = '\0';
	return (str_malloc);
}

static char	*return_next_line(char **s)
{
	char	*out;
	char	*tmp;
	int		len;

	len = 0;
	out = NULL;
	while ((*s)[len] != '\n' && (*s)[len])
		len++;
	if ((*s)[len] == '\n')
	{
		out = ft_substr(*s, 0, len + 1);
		tmp = ft_strdup(*s + len + 1);
		free(*s);
		*s = tmp;
		if (!**s)
		{
			free(*s);
			*s = NULL;
		}
		return (out);
	}
	out = ft_strdup(*s);
	free(*s);
	*s = NULL;
	return (out);
}

static char	*check_and_return(char **s, ssize_t n, int fd)
{
	if (n < 0)
		return (NULL);
	if (!n && (!s[fd] || !*s[fd]))
		return (NULL);
	return (return_next_line(&s[fd]));
}

char	*ft_get_next_line(int fd)
{
	char		*tmp;
	char		*buf;
	static char	*s[1];
	int			n;

	if (fd < 0 || BUFFER_SIZE <= 0)
		return (NULL);
	buf = malloc(BUFFER_SIZE + 1);
	if (!buf)
		return (NULL);
	n = read(fd, buf, BUFFER_SIZE);
	while (n > 0)
	{
		buf[n] = '\0';
		if (!s[fd])
			s[fd] = ft_strdup("");
		tmp = ft_strjoin(s[fd], buf);
		free(s[fd]);
		s[fd] = tmp;
		if (ft_strchr(buf, '\n'))
			break ;
		n = read(fd, buf, BUFFER_SIZE);
	}
	free(buf);
	return (check_and_return(s, n, fd));
}
